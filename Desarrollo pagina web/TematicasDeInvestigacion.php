					<p><strong>Radio-Propagacion:</strong> </br></br>
            La propagación de ondas de radio o radiopropagación es el comportamiento de las ondas de radio (electromagnéticas) cuando se trasladan por el espacio. Se transmiten, reciben o propagan desde un punto sobre la Tierra a otro, a la atmósfera o al espacio.</br></br>

            La propagación de ondas electromagnéticas por el espacio libre se suele llamar propagación de radio frecuencia o simplemente radio-propagación, aunque en el espacio libre implica el vacío; las ondas de radio transmitida por la fuente se propagan por
            la atmoósfera terrestre, posteriormente se recibe en la antena receptora, la radiación y la captura de esta son funciones de las antenas y de la distancia entre ellas.</br></br>

            Las ondas de radio en diferentes frecuencias se propagan de diferentes maneras. En frecuencias extremadamente bajas (ELF) y frecuencias muy bajas (VLF), la longitud de onda es mucho mayor que la separación entre la superficie de la Tierra y la capa D
            de la ionosfera, por lo que las ondas electromagnéticas pueden propagarse en esta región como una guía de ondas. De hecho, para frecuencias inferiores a 20 kHz, la onda se propaga como un modo de guía de onda única con un campo magnético horizontal y
            un campo eléctrico vertical. La interacción de las ondas de radio con las regiones ionizadas de la atmósfera hace que la propagación de radio sea más compleja de predecir y analizar que en el espacio libre. La propagación ionosférica de radio tiene una
            fuerte conexión con los fenómenos espaciales.</br></br>


